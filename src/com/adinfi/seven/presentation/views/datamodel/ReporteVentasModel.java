package com.adinfi.seven.presentation.views.datamodel;

public class ReporteVentasModel {
	private String sku;
	private String fechaIni;
	private String fechaFin;
	private String ventaUni;
	private String ventaPes;
	
	public String getSku() {
		return sku;
	}
	public void setSku(String sku) {
		this.sku = sku;
	}
	public String getFechaIni() {
		return fechaIni;
	}
	public void setFechaIni(String fechaIni) {
		this.fechaIni = fechaIni;
	}
	public String getFechaFin() {
		return fechaFin;
	}
	public void setFechaFin(String fechaFin) {
		this.fechaFin = fechaFin;
	}
	public String getVentaUni() {
		return ventaUni;
	}
	public void setVentaUni(String ventaUni) {
		this.ventaUni = ventaUni;
	}
	public String getVentaPes() {
		return ventaPes;
	}
	public void setVentaPes(String ventaPes) {
		this.ventaPes = ventaPes;
	}
	
	
}
