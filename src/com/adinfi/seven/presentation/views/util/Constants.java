    public static EtiquetaDTO getEtiquetaCode(int idetiqueta){
        EtiquetaDTO etiqueta = new EtiquetaDTO();
        etiqueta.setId(idetiqueta);
        switch (idetiqueta){
            case 1: 
                etiqueta.setCodigo("#0000FF");
                etiqueta.setDescription("Azul");
                break;
            case 2: 
                etiqueta.setCodigo("#A113B1");
                etiqueta.setDescription("Morado");
                break;
            case 3: 
                etiqueta.setCodigo("#FF0000");
                etiqueta.setDescription("Rojo");
                break;
            case 4: 
                etiqueta.setCodigo("#A8A3A3");
                break;
            default: 
                etiqueta.setCodigo("#000000");
                etiqueta.setDescription("Negro");
                break;
        }
        return etiqueta;
    }

