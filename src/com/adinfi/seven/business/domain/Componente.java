package com.adinfi.seven.business.domain;

public class Componente {
	
	private int idComponente;
	private int idLista;
	private String code;
	private int idCategory;
	private int idSubCategory;
	private String SKU;
	private String UPC;
	
	public Componente(){
		
	}
	
	public Componente(int idLista, String code, int idCategory, int idSubCategory, String SKU, String UPC){
		this.idLista = idLista;
		this.code = code;
		this.idCategory = idCategory;
		this.idSubCategory = idSubCategory;
		this.SKU = SKU;
		this.UPC = UPC;
	}
	
	/**
	 * @return the idComponente
	 */
	public int getIdComponente() {
		return idComponente;
	}
	/**
	 * @param idComponente the idComponente to set
	 */
	public void setIdComponente(int idComponente) {
		this.idComponente = idComponente;
	}
	/**
	 * @return the idLista
	 */
	public int getIdLista() {
		return idLista;
	}
	/**
	 * @param idLista the idLista to set
	 */
	public void setIdLista(int idLista) {
		this.idLista = idLista;
	}
	/**
	 * @return the code
	 */
	public String getCode() {
		return code;
	}
	/**
	 * @param code the code to set
	 */
	public void setCode(String code) {
		this.code = code;
	}
	/**
	 * @return the idCategory
	 */
	public int getIdCategory() {
		return idCategory;
	}
	/**
	 * @param idCategory the idCategory to set
	 */
	public void setIdCategory(int idCategory) {
		this.idCategory = idCategory;
	}
	/**
	 * @return the idSubCategory
	 */
	public int getIdSubCategory() {
		return idSubCategory;
	}
	/**
	 * @param idSubCategory the idSubCategory to set
	 */
	public void setIdSubCategory(int idSubCategory) {
		this.idSubCategory = idSubCategory;
	}
	/**
	 * @return the sKU
	 */
	public String getSKU() {
		return SKU;
	}
	/**
	 * @param sKU the sKU to set
	 */
	public void setSKU(String sKU) {
		SKU = sKU;
	}
	/**
	 * @return the uPC
	 */
	public String getUPC() {
		return UPC;
	}
	/**
	 * @param uPC the uPC to set
	 */
	public void setUPC(String uPC) {
		UPC = uPC;
	}

}
