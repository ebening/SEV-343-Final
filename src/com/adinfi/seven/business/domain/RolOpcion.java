package com.adinfi.seven.business.domain;

public class RolOpcion {
	Integer rolOpcId = null;
	String role = null;
	Opcion opcion = null;

	/**
	 * @return the rolOpcId
	 */
	public Integer getRolOpcId() {
		return rolOpcId;
	}

	/**
	 * @param rolOpcId
	 *            the rolOpcId to set
	 */
	public void setRolOpcId(Integer rolOpcId) {
		this.rolOpcId = rolOpcId;
	}

	/**
	 * @return the role
	 */
	public String getRole() {
		return role;
	}

	/**
	 * @param role
	 *            the role to set
	 */
	public void setRole(String role) {
		this.role = role;
	}

	/**
	 * @return the opcion
	 */
	public Opcion getOpcion() {
		return opcion;
	}

	/**
	 * @param opcion
	 *            the opcion to set
	 */
	public void setOpcion(Opcion opcion) {
		this.opcion = opcion;
	}
}
