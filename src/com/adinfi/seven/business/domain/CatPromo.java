package com.adinfi.seven.business.domain;

@SuppressWarnings("serial")
public class CatPromo implements java.io.Serializable {

	private int idPromo;
	private CatTipoPromo catTipoPromo;
	private String nombre;

	public CatPromo() {
	}

	public CatPromo(int idPromo) {
		this.idPromo = idPromo;
	}

	public CatPromo(int idPromo, CatTipoPromo catTipoPromo, String nombre) {
		this.idPromo = idPromo;
		this.catTipoPromo = catTipoPromo;
		this.nombre = nombre;
	}

	public int getIdPromo() {
		return this.idPromo;
	}

	public void setIdPromo(int idPromo) {
		this.idPromo = idPromo;
	}


	public CatTipoPromo getCatTipoPromo() {
		return this.catTipoPromo;
	}

	public void setCatTipoPromo(CatTipoPromo catTipoPromo) {
		this.catTipoPromo = catTipoPromo;
	}

	public String getNombre() {
		return this.nombre;
	}

	public void setNombre(String nombre) {
		this.nombre = nombre;
	}

}